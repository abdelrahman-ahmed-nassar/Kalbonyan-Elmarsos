
# * list

'''
in list every item have an index number 
Notice:
  the index in programming start from zero 

you can access an item in the list you write the list name followed by [the index of the item]
'''

cities = [
    'Tokyo',
    'Dakar',
    'Mumbai',
    'Buenos Aires',
]

print(cities[1])
# the result will be => Dakar


